def ns_to_ms(ns):
    return ns / 1000.0 / 1000.0

def nslist_to_mslist(nslist):
    return list(map(ns_to_ms, nslist))

def ns_to_gps(ns, bytes_touched):
    s = ns / 1000 / 1000 / 1000
    gb = bytes_touched / 1024 / 1024 / 1024
    gps = gb / s
    return gps

